public class Task {

    private String Name;

    private String dueDate;

    private String course;

    private int rating;

    Task(String Name, String dueDate, String course, int rating) {
        this.Name = Name;
        this.course = course;
        this.dueDate = dueDate;
        this.course = course;
        this.rating = rating;
    }

}
